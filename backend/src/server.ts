import "dotenv/config" ;
import {app} from "./app" ;
import {connectDB} from "./database/connect" ;

const PORT = process.env.PORT || 5000

const startServer = async () => {
    try {
        await connectDB()
        app.listen(PORT , () => {
            console.log(`Server is running on port ${PORT}`)
        })
    } catch (error) {
        console.error("Error starting server", error)
        process.exit(1)
    }
}

startServer()